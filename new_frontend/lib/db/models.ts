export interface MedicalRecord {
  _id?: ObjectId
  patientId: ObjectId
  uploadedBy: ObjectId
  uploaderRole: "patient" | "doctor" | "lab"
  fileName: string

  // Pinata CID (preferred). Some older routes use mock_* fields.
  cid?: string
  fileCID?: string

  fileSize?: number
  fileType?: string
  fileUrl?: string
  fileHash?: string

  recordType: string
  description?: string
  metadata?: Record<string, any>

  // Different routes use different timestamp field names.
  createdAt?: Date
  uploadDate?: Date

  fileId: number , // The ID generated by EHRRegistry.sol
  transactionHash: string  // The real Quorum tx hash
    // ── Encryption fields ──────────────────────────────────────
    encryptedAESKey?: string            // ← ADD: AES key wrapped with patient's RSA public key
    aesIV?: string                      // ← ADD: base64 IV used in AES-GCM
    doctorKeys?: Record<string, string> // ← ADD: { doctorAddress → encrypted AES key for that doctor }
}

export interface AccessPermission {
  _id: string
  patientId: string
  grantedTo: string // userId of doctor or lab
  grantedToRole: "doctor" | "lab"
  accessLevel: "view" | "upload" | "view-upload" // Labs typically only have upload
  grantedAt: Date
  revokedAt?: Date
  isActive: boolean
  blockchainTxHash?: string // Reference to blockchain transaction
}

export interface AISummary {
  _id: string
  recordId: string
  patientId: string
  summary: {
    diagnosis?: string
    medications?: string[]
    testResults?: Array<{
      test: string
      value: string
      unit?: string
      normalRange?: string
    }>
    recommendations?: string[]
    keyFindings: string[]
  }
  generatedAt: Date
  modelUsed: string
}

export interface AuditLog {
  _id: string
  action: "grant_access" | "revoke_access" | "view_record" | "upload_record" | "download_record"
  performedBy: string // userId
  performedByRole: "patient" | "doctor" | "lab" | "admin"
  targetUserId?: string // For access grants/revokes
  recordId?: string // For record operations
  patientId: string
  timestamp: Date
  ipAddress?: string
  blockchainTxHash?: string
  metadata?: Record<string, any>
}

export interface Followup {
  _id?: ObjectId | string
  patientId: ObjectId | string
  doctorId: ObjectId | string
  doctorName: string
  doctorSpecialization: string
  action: "view" | "upload" | "observation"
  recordId?: string
  description: string
  timestamp: Date
}
