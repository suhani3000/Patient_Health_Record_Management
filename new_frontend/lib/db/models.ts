// MongoDB Schema Definitions for Healthcare EHR Platform
import mongoose from "mongoose";
import { ObjectId } from "mongodb";
export interface User {
  // In MongoDB this is typically an ObjectId, but parts of the codebase
  // also treat it as a string (after JSON serialization).
  _id: ObjectId | string
  email: string
  password: string // hashed
  name: string
  role: "patient" | "doctor" | "lab" | "admin"
  isVerified: boolean // For doctor/lab approval by admin
  isBlocked: boolean
  specialization?: string // For doctors
  licenseNumber?: string // For doctors and labs
  createdAt: Date
  updatedAt: Date
  blockchainAddress?: string // Thirdweb In-App Wallet address (unique, sparse index in MongoDB)
  encryptionPublicKey?: string
  /**
   * Server-side escrow for the RSA private key so decryption can work on any device.
   * Stored encrypted by server secret (see /api/auth/unified).
   */
  encryptionPrivateKeyEscrow?: {
    ivB64: string
    tagB64: string
    cipherB64: string
  }
  /** Patient profile — set during universal intake */
  dateOfBirth?: string
  bloodType?: string
  emergencyContact?: string
}

